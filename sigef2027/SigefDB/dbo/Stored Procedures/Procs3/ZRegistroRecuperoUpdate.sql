		ID_ATTO_RITIRO = @IdAttoRitiro, 
		ID_ATTO_NON_RECUPERABILITA = @IdAttoNonRecuperabilita, 
		RECUPERABILE = @Recuperabile, 
		IMPORTO_DETRATTO_TOTALE = @ImportoDetrattoTotale, 
		DATA_CERTIFICAZIONE_NON_RECUPERABILITA = @DataCertificazioneNonRecuperabilita, 
		DATA_REGISTRAZIONE_NON_RECUPERABILITA = @DataRegistrazioneNonRecuperabilita, 
