		IMPORTO_DA_RECUPERARE_TOTALE = @ImportoDaRecuperareTotale, 
		IMPORTO_DETRATTO_PRIVATO = @ImportoDetrattoPrivato, 
		IMPORTO_RECUPERATO_PRIVATO = @ImportoRecuperatoPrivato, 
		IMPORTO_RECUPERATO_TOTALE = @ImportoRecuperatoTotale, 
