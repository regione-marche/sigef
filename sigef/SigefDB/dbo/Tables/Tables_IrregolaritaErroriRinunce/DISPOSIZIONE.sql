CREATE TABLE [dbo].[DISPOSIZIONE] (
    [ID_DISPOSIZIONE]          INT            IDENTITY (1, 1) NOT NULL,
    [DATA_INSERIMENTO]         DATETIME       CONSTRAINT [DF_DISPOSIZIONE_DATA_INSERIMENTO] DEFAULT (getdate()) NULL,
    [CF_INSERIMENTO]           VARCHAR (16)   NULL,
    [DATA_MODIFICA]            DATETIME       CONSTRAINT [DF_DISPOSIZIONE_DATA_MODIFICA] DEFAULT (getdate()) NULL,
    [CF_MODIFICA]              VARCHAR (16)   NULL,
    [ID_TIPO_DISPOSIZIONE]     INT            NULL,
    [NUMERO]                   VARCHAR (50)   NULL,
    [ANNO]                     INT            NULL,
    [ARTICOLO_PARAGRAFO]       VARCHAR (50)   NULL,
    [DISPOSIZIONE_NAZIONALE]   VARCHAR (2000) NULL,
