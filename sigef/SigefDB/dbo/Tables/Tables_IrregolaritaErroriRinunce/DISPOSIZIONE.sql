    CONSTRAINT [FK_DISPOSIZIONE_CODIFICA_GENERICA] FOREIGN KEY ([ID_TIPO_DISPOSIZIONE]) REFERENCES [dbo].[CODIFICA_GENERICA] ([ID]),
    CONSTRAINT [FK_DISPOSIZIONE_REGISTRO_IRREGOLARITA] FOREIGN KEY ([ID_REGISTRO_IRREGOLARITA]) REFERENCES [dbo].[REGISTRO_IRREGOLARITA] ([ID_IRREGOLARITA]),
	CONSTRAINT [FK_DISPOSIZIONE_IRREGOLARITA] FOREIGN KEY ([ID_IRREGOLARITA]) REFERENCES [dbo].[IRREGOLARITA] ([ID_IRREGOLARITA])
