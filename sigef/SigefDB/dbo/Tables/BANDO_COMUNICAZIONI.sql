CREATE TABLE [dbo].[BANDO_COMUNICAZIONI] (
    [COD_TIPO]              CHAR (3) NOT NULL,
    [ID_BANDO]              INT      NOT NULL,
    [TESTO]                 NTEXT    NULL,
    [MODALITA_ANTICIPO]     NTEXT    NULL,
