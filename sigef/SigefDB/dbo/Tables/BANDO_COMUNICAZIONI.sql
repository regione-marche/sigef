    [ID_ATTO]               INT      NULL,
    [DATA]                  DATETIME NULL,
    CONSTRAINT [PK_BANDO_COMUNICAZIONI] PRIMARY KEY CLUSTERED ([COD_TIPO] ASC, [ID_BANDO] ASC) WITH (FILLFACTOR = 80),
