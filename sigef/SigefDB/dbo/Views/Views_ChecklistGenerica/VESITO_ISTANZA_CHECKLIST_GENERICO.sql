
CREATE VIEW VESITO_ISTANZA_CHECKLIST_GENERICO
AS
SELECT EIC.ID_ESITO_GENERICO, 
	EIC.ID_VOCE_GENERICA, 
	EIC.ID_ISTANZA_GENERICA, 
	EIC.COD_ESITO, 
	EIC.DATA_INSERIMENTO, 
	EIC.CF_INSERIMENTO, 
	EIC.DATA_MODIFICA, 
	EIC.CF_MODIFICA, 
	EIC.NOTE, 
	EIC.COD_ESITO_REVISORE, 
	EIC.DATA_REVISORE, 
	EIC.REVISORE, 
	EIC.NOTE_REVISORE, 
	EIC.ESCLUDI_DA_COMUNICAZIONE, 
	ES.DESCRIZIONE, 
	ES.ESITO_POSITIVO, 
	ES_REVISORE.DESCRIZIONE AS DESCRIZIONE_ESITO_REVISORE, 
	ES_REVISORE.ESITO_POSITIVO AS ESITO_POSITIVO_REVISORE, 
	VG.DESCRIZIONE AS DESCRIZIONE_VOCE_GENERICA, 
	VG.AUTOMATICO, 
	VG.QUERY_SQL, 
	VG.QUERY_SQL_POST, 
	VG.CODE_METHOD, 
	VG.URL, 
	VG.VAL_MINIMO, 
	VG.VAL_MASSIMO, 
	VXC.ORDINE, 
	VXC.OBBLIGATORIO, 
	VXC.PESO, 
	CG.ID_CHECKLIST_GENERICA, 
	CG.DESCRIZIONE AS DESCRIZIONE_CHECKLIST_GENERICA, 
	EIC.VALORE, 
	VG.VAL_ESITO_NEGATIVO, 
	VXC.MISURA, 
	VXC.ID_VOCE_X_CHECKLIST_GENERICA
FROM ESITO_ISTANZA_CHECKLIST_GENERICO EIC 
	JOIN VOCE_GENERICA VG ON EIC.ID_VOCE_GENERICA = VG.ID_VOCE_GENERICA 
	JOIN ISTANZA_CHECKLIST_GENERICA ICG ON EIC.ID_ISTANZA_GENERICA = ICG.ID_ISTANZA_GENERICA
	JOIN CHECKLIST_GENERICA CG ON ICG.ID_CHECKLIST_GENERICA = CG.ID_CHECKLIST_GENERICA 
	JOIN VOCE_X_CHECKLIST_GENERICA VXC ON VG.ID_VOCE_GENERICA = VXC.ID_VOCE_GENERICA AND CG.ID_CHECKLIST_GENERICA = VXC.ID_CHECKLIST_GENERICA 
	LEFT JOIN ESITI_STEP ES ON EIC.COD_ESITO = ES.COD_ESITO 
	LEFT JOIN ESITI_STEP ES_REVISORE ON EIC.COD_ESITO_REVISORE = ES_REVISORE.COD_ESITO